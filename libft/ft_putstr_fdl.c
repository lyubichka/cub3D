#include "libft.h"

int	ft_putstr_fdl(char *s, int fd)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (ft_putchar_fdl(s[i], fd) == -1)
			return (-1);
		i++;
	}
	return (0);
}
